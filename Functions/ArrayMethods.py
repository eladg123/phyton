L = ["Hello","Shalom","Marhaba"]

L.append("Bonjour") # L = ["Hello","Shalom","Marhaba","Bonjour"]

L.remove('Hello') # L = ["Shalom","Marhaba","Bonjour"]   /remove only one time even there is more "Hello" in the array

L.insert(2,"Hola") # L = ["Shalom","Marhaba","Hola","Bonjour"]

L.reverse() # L = ["Bonjour","Hola","Marhaba","Shalom"]

